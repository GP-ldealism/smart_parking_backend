-- 3. 车位批量插入存储过程
-- 按照A-Z + 001-999 编号规则
-- =====================================================

DELIMITER //

CREATE PROCEDURE batch_insert_parking_spaces(
    IN p_parking_lot_id INT,
    IN p_total_spaces INT,
    IN p_create_by INT
)
BEGIN
    DECLARE v_zone CHAR(1);
    DECLARE v_zone_num INT;
    DECLARE v_space_no VARCHAR(10);
    DECLARE v_type INT;
    DECLARE v_status INT;
    DECLARE v_counter INT DEFAULT 1;
    
    -- 循环区域 A-Z
    SET v_zone_num = 65; -- ASCII 'A'
    
    WHILE v_zone_num <= 90 AND v_counter <= p_total_spaces DO
        SET v_zone = CHAR(v_zone_num);
        
        -- 每个区域生成最多999个车位
        WHILE v_counter <= p_total_spaces AND v_counter <= (v_zone_num - 64) * 999 DO
            -- 生成车位号
            SET v_space_no = CONCAT(v_zone, LPAD(v_counter - ((v_zone_num - 65) * 999), 3, '0'));
            
            -- 随机分配车位类型 (0=普通 1=新能源 2=VIP 3=其他)
            SET v_type = FLOOR(RAND() * 4);
            
            -- 随机分配状态 (0=占用 1=空闲)，70%空闲
            SET v_status = CASE WHEN RAND() > 0.3 THEN 1 ELSE 0 END;
            
            -- 插入车位
            INSERT INTO parking_space (
                parking_lot_id, space_no, type, status, create_by, create_time, update_time
            ) VALUES (
                p_parking_lot_id, v_space_no, v_type, v_status, p_create_by, NOW(), NOW()
            );
            
            SET v_counter = v_counter + 1;
        END WHILE;
        
        SET v_zone_num = v_zone_num + 1;
    END WHILE;
    
    -- 更新停车场的空闲车位数
    UPDATE parking_lot 
    SET free_space = (
        SELECT COUNT(*) 
        FROM parking_space 
        WHERE parking_lot_id = p_parking_lot_id AND status = 1
    )
    WHERE id = p_parking_lot_id;
    
    SELECT CONCAT('成功为停车场ID ', p_parking_lot_id, ' 插入 ', p_total_spaces, ' 个车位') AS result;
END //

DELIMITER ;

